//make sectionId available in a request
exports.specificById = (req, res, next, id) => {
    Specific.findById(id)
      .exec((err, specific) => {
        if (err) {
          return res.status(400).json({
            error: errorHandler(err),
          });
        }
        req.specific = specific;
        next();
      });
  };
  
  exports.create = (req, res) => {
    let form = new formidable.IncomingForm();
    form.keepExtensions = true;
    form.parse(req, (err, fields) => {
      const { subject, section, identifier, introduction, title, body } = fields;
  
      if (!title || title.length === 0) {
        return res.status(400).json({
          error: "A title is required",
        });
      }
    
      let specific = new Specific();
      specific.subject = subject;
      specific.section = section;
      specific.identifier = identifier;
      specific.introduction = introduction;
      specific.title = title;
      
      specific.body = body;
            
      specific.slug = slugify(title).toLowerCase();

      specific.save((err, data) => {
        if (err) {
          return res.status(400).json({
            error: errorHandler(err),
          });
        }
        res.json(data)
      });
    });
  };

  exports.getSpecific = (req, res) => {
    const slug = req.params.slug.toLowerCase();
    const { subject } = req.body.syllabus;
    //console.log(subject)
  
    Section.findOne({ slug }).exec((err, section) => {         
      if (err) {
        return res.status(400).json({
          error: errorHandler(err),
        });
      }
   
      //onClick for the foreword base on the section and the syllabus
      Specific.find({ section: section, subject: { $in: subject } })
        .exec((err, data) => {
          if (err) {
            return res.status(400).json({
              error: errorHandler(err),
            });
          }
          res.json(data) 
            
        });        
    });
  };
  